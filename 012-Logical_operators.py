# Chaining comparison operators

print(1 < 2 < 3)

#* and

print(1 < 2 and 2 < 3)

print ('h' == 'h' and 2 == 2)

print (('h' == 'h') and (2 == 2))

#* or

print ( (100 == 1) or 2 == 2 )
print ( (100 == 1) or 1 == 2 )

#* not

print(not(1== 1))
print(not 400 > 5000)